package com.handler;

public class Point {

        private int startPointX1=0;
        private int startPointY1=0;
        private int endPointX2=0;
        private int endPointY2=0;
        private int width;
        private int height;

        public Point()
        {

        }

        public int getStartPointY1() {
            return startPointY1;
        }

        public void setStartPointY1(int startPointY1) {
            this.startPointY1 = startPointY1;
        }

        public int getStartPointX1() {
            return startPointX1;
        }

        public void setStartPointX1(int startPointX1) {
            this.startPointX1 = startPointX1;
        }

        public int getEndPointX2() {
            return endPointX2;
        }

        public void setEndPointX2(int endPointX2) {
            this.endPointX2 = endPointX2;
        }

        public int getEndPointY2() {
            return endPointY2;
        }

        public void setEndPointY2(int endPointY2) {
            this.endPointY2 = endPointY2;
        }

        public int getWidth() {
            return width;
        }

        public void setWidth(int width) {
            this.width = width;
        }

        public int getHeight() {
            return height;
        }

        public void setHeight(int height) {
            this.height = height;
        }

    }


